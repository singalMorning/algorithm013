# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def mergeTwoLists(self, l1: ListNode, l2: ListNode) -> ListNode:
        l3 = []
        while l1:
            l3.append(l1.val)
            l1 = l1.next
        while l2:
            l3.append(l2.val)
            l2 = l2.next
        l3.sort()

        l4 = ListNode(0)
        curr = l4
        for i in l3:
            node = ListNode(i)
            curr.next = node
            curr = curr.next
        return l4.next


        
